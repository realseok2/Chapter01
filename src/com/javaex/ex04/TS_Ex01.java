package com.javaex.ex04;

public class TS_Ex01 {

	public static void main(String[] args) {

		int i = 0;																															 //		i의 초기값을 설정

		while (i < 5) {																												//		반복조건을 설정
			System.out.println("Hello everyone!");
			i++;																																//		증감식 ( 탈출조건 )
		}
	}																																				//		while문에는 위 세가지 조건들이 항상 포함되어야 한다. 따라서 가장 중요하므로 익혀두도록 하자!

}
